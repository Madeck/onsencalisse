var stamping = false;

var previousMouseX;
var previousMouseY;

$(function() {
	windowSize();
	setTimeout("addImg()", 200);
	setTimeout("addStamp()", 2400);
});

function addImg() {
	var img = $("#calisse");
	var imgHeight = img.height();
	var imgWidth  = img.width();

	img.offset({ top : (windowHeight - imgHeight) / 2 , left : (windowWidth - imgWidth) / 2 })

	img.delay(400).animate({ 'opacity' : 1 } , 800);
}

function addStamp(offsetLeft, offsetTop) {

	if( ! offsetTop )  offsetTop  = windowHeight / 2 ;
	if( ! offsetLeft ) offsetLeft = windowWidth / 2 ;

	offsetLeft -= 124;
	offsetTop  -= 64;

	var angle = Math.floor( Math.random() * 40 - 20 );

	var newStamp = $("<div class='stamp'><div class='on'>ON</div><div class='sen'>S'EN</div><div class='calisse'>CÂLISSE</div></div>");
	var newShard = $("<img class='shards' src='img/shards.png' />");

	// newStamp = $('<object class="stamp" type="image/svg+xml" width="248" height="128" data="img/stamp.svg"> <span/> </object>');

	newStamp.css({
		'opacity' : 0.25,
		'top' : offsetTop - windowHeight - 170,
		'left' : offsetLeft,

		'-webkit-transform'	: 'rotate(' + angle + 'deg)', // Safari and Chrome
		   '-moz-transform'	: 'rotate(' + angle + 'deg)', // Firefox
		    '-ms-transform'	: 'rotate(' + angle + 'deg)', // IE 9
		     '-o-transform'	: 'rotate(' + angle + 'deg)', // Opera
		   		'transform'	: 'rotate(' + angle + 'deg)',
	});

	newStamp.appendTo($("body"));

	newStamp.animate( { 'top' : offsetTop, 'opacity' : '1' }, 200, 'easeInCubic', function() {
		bodyBounce();
		/*
		newShard.css({
			'-webkit-transform'	: 'rotate(' + angle + 'deg)', // Safari and Chrome
			   '-moz-transform'	: 'rotate(' + angle + 'deg)', // Firefox
			    '-ms-transform'	: 'rotate(' + angle + 'deg)', // IE 9
			     '-o-transform'	: 'rotate(' + angle + 'deg)', // Opera
			   		'transform'	: 'rotate(' + angle + 'deg)',
		});
		*/
		newShard.css({
			'top' : offsetTop - 36 + Math.floor(Math.random()*20) , // 52 margin
			'left' : offsetLeft - 26 + Math.floor(Math.random()*20) // 72 margin
		}).appendTo($("body"));
	} );

	// setTimeout("addStamp()", 10);
}

$(window).mouseup( function(e) {
	var currentMouseX = e.clientX;
	var currentMouseY = e.clientY;
	
	// takes care of people hitting the mouse at the same spot.
	if( e.clientX == previousMouseX && e.clientY == previousMouseY && stamping) {
		addStamp(
			currentMouseX - 20 + Math.floor(Math.random()*20),
			currentMouseY - 20 + Math.floor(Math.random()*20)
		);
	}

	else if(stamping) addStamp(currentMouseX, currentMouseY);

	previousMouseX = e.clientX;
	previousMouseY = e.clientY;
});

$("#stampTool").mouseup( function(e) {
	e.stopPropagation();

	( stamping ) ? stamping = false : stamping = true;
	$(this).toggleClass("active");
})

$("body").delegate('img', 'dragstart', function(e){
	e.preventDefault();
})

$(window).resize(function() {
	windowSize();
});

function bodyBounce() {
	$("body").css({ "top" : "+=4" , "background-position-y" : "+=4" });
	$("body").animate({ "top" : 0 , "background-position-y" : 0 }, 80 );
}

function windowSize() {
	windowHeight = $(window).height();
	windowWidth  = $(window).width();
};











